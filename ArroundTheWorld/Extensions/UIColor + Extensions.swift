import Foundation
import UIKit

extension UIColor{
    class func blackTitle() -> UIColor{
        return UIColor(red: 0.141, green: 0.141, blue: 0.141, alpha: 1)
    }
    
    class func grayTitles() -> UIColor{
        return UIColor(red: 0.141, green: 0.141, blue: 0.141, alpha: 0.4)
    }
}
